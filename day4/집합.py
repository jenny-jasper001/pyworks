#요소추가
set1 = {1, 2, 3}
set1.add(4)
print(set1)

#요소 삭제 - remove 특정한거 지울때 
set1.remove(3)
print(set1)

#어떤 요소의 존재 확인
print(3 in set1) #존재가 있는지 없는지 확인이라 true, false로 나온다 3을 삭제해서 안에 없으니까 false가 출력이 된다

